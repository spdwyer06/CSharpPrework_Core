using System;

/* ARITHMETIC OPERATORS */

int additionResult = 5 + 2;
Console.WriteLine("5 + 2 = " + additionResult);

int subtractionResult = 5 - 2;
Console.WriteLine("5 - 2 = " + subtractionResult);

int muliplicationResult = 5 * 2;
Console.WriteLine("5 * 2 = " + muliplicationResult);

int divisionResult = 5 / 2;
Console.WriteLine("5 / 2 = " + divisionResult);

int remainderResult = 5 % 2;
Console.WriteLine("5 % 2 = " + remainderResult);

int noRemainder = 9 & 3;
Console.WriteLine("9 % 3 = " + noRemainder);